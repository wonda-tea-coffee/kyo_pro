ll mod_pow(ll x, ll n, ll m) {
  ll res = 1;
  while (n > 0) {
    if (n & 1) res = res * x % m;
    x = x * x % m;
    n >>= 1;
  }
  return res;
}

ll modinv(ll a, ll m) {
  ll b = m, u = 1, v = 0;
  while (b) {
    ll t = a / b;
    a -= t * b; swap(a, b);
    u -= t * v; swap(u, v);
  }
  u %= m;
  if (u < 0) u += m;
  return u;
}

void solve() {
  ll s, a, b, n; cin >> s >> a >> b >> n;
  ll v = mod_pow(a + 1, n, MOD);
  ll ans = (s * v % MOD) + (((b * modinv(a, MOD)) % MOD) * (v - 1)) % MOD;
  outl(ans % MOD);
}

